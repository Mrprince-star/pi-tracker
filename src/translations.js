/** Returns a translation function bound to a given language code. */
export function getTranslator(lang) {
  const dict = translations[lang] || translations.en;
  return (key, vars) => {
    let str = dict[key] ?? translations.en[key] ?? key;
    if (vars) {
      Object.keys(vars).forEach((k) => {
        str = str.replace(`{${k}}`, vars[k]);
      });
    }
    return str;
  };
}

/** Detects the best supported language from the browser, falling back to English. */
export function detectLanguage() {
  if (typeof navigator === "undefined") return "en";
  const browserLangs = navigator.languages || [navigator.language || "en"];
  for (const bl of browserLangs) {
    const code = bl.toLowerCase().split("-")[0];
    if (translations[code]) return code;
    // Filipino sometimes reports as "tl" (Tagalog) at the browser/OS level.
    if (code === "tl" && translations.fil) return "fil";
  }
  return "en";
}
